    <p style="font-size: 16px; margin-bottom: 20px;">
        Getting started with composting in your community is easy. Here's a quick guide to how our program works:
    </p>

    <h3 style="color: #388e3c; margin-top: 25px;">1. Collect Your Compostables</h3>
    <p>Gather kitchen and garden waste such as fruit peels, vegetable scraps, coffee grounds, and dried leaves. Avoid meat, dairy, or oily food.</p>

    <h3 style="color: #388e3c; margin-top: 25px;">2. Drop-Off or Schedule a Pickup</h3>
    <p>We offer two convenient options:</p>
    <ul style="padding-left: 20px;">
        <li><strong>Drop-Off:</strong> Find the nearest compost bin using our <a href="locator.php" style="color: #2e7d32; text-decoration: underline;">Compost Bin Locator</a>.</li>
        <li><strong>Pickup:</strong> Schedule a pickup through your <a href="dashboard/user_dashboard.php" style="color: #2e7d32; text-decoration: underline;">User Dashboard</a> (available for members only).</li>
    </ul>

    <h3 style="color: #388e3c; margin-top: 25px;">3. Composting Process</h3>
    <p>Once collected, your waste is taken to our community composting centers where it’s turned into rich, organic compost using sustainable methods.</p>

    <h3 style="color: #388e3c; margin-top: 25px;">4. Reuse & Reward</h3>
    <p>The finished compost is distributed to local gardens, schools, farms, and even back to our contributors! Plus, you earn reward points for each contribution.</p>

    <h3 style="color: #388e3c; margin-top: 25px;">What You Can Compost ✅</h3>
    <ul style="padding-left: 20px;">
        <li>Fruit and vegetable scraps</li>
        <li>Eggshells</li>
        <li>Tea bags and coffee grounds</li>
        <li>Dry leaves and grass clippings</li>
        <li>Shredded paper and cardboard</li>
    </ul>

    <h3 style="color: #388e3c; margin-top: 25px;">What NOT to Compost ❌</h3>
    <ul style="padding-left: 20px;">
        <li>Meat or bones</li>
        <li>Dairy products</li>
        <li>Oily or greasy food</li>
        <li>Plastic or synthetic materials</li>
        <li>Diseased plants</li>
    </ul>

